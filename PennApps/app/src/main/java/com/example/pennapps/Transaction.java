package com.example.pennapps;

public class Transaction {
    private int amount;
    private int day;
    private int year;
    private int month;
    private String type;

    public String getType() {
        return type;
    }

    public int getYear() {
        return year;
    }

    public int getMonth() {
        return month;
    }

    public int getDay() {
        return day;
    }

    public int getAmount() {
        return amount;
    }
}
